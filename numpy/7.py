import numpy as np

# Create two matrices as NumPy arrays
matrix1 = np.array([[1, 2], [3, 4]])
matrix2 = np.array([[5, 6], [7, 8]])

# Perform matrix multiplication
result_matrix = np.dot(matrix1, matrix2)

# Print the result
print("Matrix 1:")
print(matrix1)

print("Matrix 2:")
print(matrix2)

print("Matrix Multiplication Result:")
print(result_matrix)
